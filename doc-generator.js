/**
 * SKYLINE AA-1 - WEEK 33
 * The Doc Generator: Automatically creates professional README.md files based on generated code and rules.
 */

class DocGenerator {
    /**
     * Generates a full README.md string.
     */
    static createREADME(files, rules, description, dependencies) {
        const timestamp = new Date().toISOString();
        
        let md = `# 🚀 Skyline AA-1 Generated Project\n\n`;
        md += `> **Generated by Skyline AA-1 v0.5** on ${timestamp}\n\n`;
        md += `## 📝 Description\n${description || 'Auto-generated validation logic and API routes.'}\n\n`;
        
        // 📂 File List
        md += `## 📂 Generated Files\n\n`;
        md += `| File | Description |\n| :--- | :--- |\n`;
        files.forEach(file => {
            const desc = this.getFileDescription(file.filename);
            md += `| \`${file.filename}\` | ${desc} |\n`;
        });
        md += `\n`;

        // 📜 Logic Rules (If available)
        if (rules && rules.length > 0) {
            md += `## 🧠 Validation Rules\n\n`;
            md += `The following logic rules were applied:\n\n`;
            md += `| Subject | Operator | Value | Type |\n| :--- | :--- | :--- | :--- |\n`;
            rules.forEach(rule => {
                md += `| \`${rule.subject}\` | \`${rule.operator}\` | \`${rule.value}\` | ${rule.dataType} |\n`;
            });
            md += `\n`;
        }

        // 📦 Dependencies
        if (dependencies && dependencies.length > 0) {
            md += `## 📦 Installation\n\n`;
            md += `Install the required dependencies:\n\n`;
            md += `\`\`\`bash\nnpm install ${dependencies.join(' ')}\n\`\`\`\n\n`;
        }

        // 💻 Usage Example
        md += `## 💻 Usage\n\n`;
        md += `\`\`\`javascript\n`;
        
        const hasValidateFile = files && files.find(f => f.filename && f.filename.includes('validate'));
        if (hasValidateFile) {
            md += `const { validateUser } = require('./validateUser');\n\n`;
            md += `// Example Valid Input\nconst validData = { email: "test@example.com", password: "secure123" };\n`;
            md += `console.log(validateUser(validData)); // { valid: true, ... }\n\n`;
            md += `// Example Invalid Input\nconst invalidData = { email: "bad-email", password: "123" };\n`;
            md += `console.log(validateUser(invalidData)); // { valid: false, ... }\n`;
        } else {
            md += `// Import your generated modules here\n`;
            md += `const app = require('./app');\n`;
        }
        md += `\`\`\`\n\n`;

        // 🛡️ Security Note
        md += `## 🛡️ Security\n`;
        md += `This code was generated with Skyline's built-in **Security Scanner** (Week 28).\n`;
        md += `- ✅ Hardcoded secrets auto-replaced with environment variables.\n`;
        md += `- ✅ Unused imports removed.\n`;
        md += `- ✅ Logical contradictions checked.\n\n`;
        md += `> ⚠️ **Note:** Always review generated code before deploying to production.\n`;

        return md;
    }

    // Helper: Guess file description based on name
    static getFileDescription(filename) {
        if (!filename) return 'Generated source code';
        if (filename.includes('validate')) return 'Validation logic for user inputs';
        if (filename.includes('route') || filename.includes('api')) return 'Express API endpoint';
        if (filename.includes('schema') || filename.includes('sql')) return 'Database schema definition';
        if (filename.includes('model')) return 'Data model definition';
        if (filename.includes('README')) return 'Project documentation';
        return 'Generated source code';
    }
}

module.exports = { DocGenerator };
